import UIKit

extension UIColor {
    public class func SBLightGrayColor() -> UIColor {
        return UIColor(red:0.91, green:0.92, blue:0.93, alpha:1)
    }
    
    public class func SBDarkGrayColor() -> UIColor {
        return UIColor(red:0.14, green:0.18, blue:0.24, alpha:1)
    }
    
    public class func SBGreenColor() -> UIColor {
        return UIColor(red:0.1, green:0.7, blue:0.58, alpha:1)
    }
}